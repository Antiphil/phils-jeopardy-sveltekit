export type Translations = typeof translations.de;

function detectLocale(): Locale {
	// 1. Saved preference takes priority
	const saved = localStorage.getItem('locale') as Locale | null;
	if (saved && saved in translations) return saved;

	// 2. Fall back to browser language — German stays German, everything else → English
	const lang = navigator.language ?? '';
	return lang.startsWith('de') ? 'de' : 'en';
}

function createLocaleStore() {
	// SSR: always start with 'de' — will be corrected on the client in $effect (see +layout.svelte)
	const { subscribe, set } = writable<Locale>('de');

	return {
		subscribe,
		set(locale: Locale) {
			if (browser) localStorage.setItem('locale', locale);
			set(locale);
		},
		/** Called once on the client after hydration to apply saved/detected locale. */
		init() {
			if (!browser) return;
			set(detectLocale());
		},
	};
}

export const locale = createLocaleStore();

export const t = derived(locale, ($locale) => translations[$locale] as Translations);
